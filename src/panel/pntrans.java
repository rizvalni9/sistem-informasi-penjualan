    private void vTabletrsKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_vTabletrsKeyPressed
        int typed = evt.getKeyCode();
        if(typed==127){
            int row = vTabletrs.getSelectedRow();
            if(vTabletrs.getSelectedRowCount()==1){
                vTabletrs.getSelectionModel().addSelectionInterval(row, row-1);
                dtm.removeRow(row);
                vTabletrs.requestFocus();
            }else{
                evt.consume();
            }
        }
    }//GEN-LAST:event_vTabletrsKeyPressed

