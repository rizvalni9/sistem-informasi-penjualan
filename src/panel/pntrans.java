    private void autoKd(){
        try{
           SimpleDateFormat ftgl = new SimpleDateFormat("yydd");
           rs=con.adapter.executeQuery("SELECT * FROM nota ORDER BY idtrs DESC");
           if(rs.next()){
               String kdTrs = rs.getString("idtrs").substring(6);
               int noTrs = (Integer.valueOf(kdTrs)+1);
               txtNotrs.setText("T"+ftgl.format(sekarang)+"N"+noTrs);
           }else{
               txtNotrs.setText("T"+ftgl.format(sekarang)+"N"+"0");
           }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "NOMOR TRANSAKSI GAGAL TAMPIL !!!","WARNING",JOptionPane.WARNING_MESSAGE);
        }
    }
    
