    private void btnBytrsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBytrsActionPerformed
        if(txtNusr.getText().equals("")||txtNplg.getText().equals("")||txtPyr.getText().equals("")||txtTotby.getText().equals("Rp. 0.00")||vTabletrs.getRowCount()==0){
            JOptionPane.showMessageDialog(null,"TRANSAKSI MASIH KOSONG !!!","WARNING",JOptionPane.WARNING_MESSAGE);  
        }else{
            int bayar = Integer.valueOf(txtPyr.getText().replace("Rp. ", "").replace(",", ""));
            int tbayar = Integer.valueOf(txtTotby.getText().replace("Rp. ", "").replace(",", "").replace(".00", ""));
            int kbayar = Integer.valueOf(txtKbn.getText().replace("Rp. ", "").replace(",","").replace(".00",""));
            if(bayar<tbayar){
                JOptionPane.showMessageDialog(null,"UANG TIDAK CUKUP !!!","WARNING",JOptionPane.WARNING_MESSAGE);
            }else{
                try{
                    con.adapter.executeUpdate("INSERT INTO nota VALUES('"+txtNotrs.getText()+"','"+txtNusr.getText()+"','"+txtNplg.getText()+"',"
                                                                    + "'"+sftgl.format(sekarang)+"','"+tbayar+"','"+bayar+"','"+kbayar+"')");
                    for(int i=0;i<vTabletrs.getRowCount();i++){
                        con.adapter.executeUpdate("INSERT INTO transaction VALUES('"+txtNotrs.getText()+"','"+vTabletrs.getValueAt(i, 0)+"',"
                                                                               + "'"+vTabletrs.getValueAt(i, 1)+"','"+vTabletrs.getValueAt(i, 2)+"','"+vTabletrs.getValueAt(i, 3)+"',"
                                                                               + "'"+vTabletrs.getValueAt(i, 4)+"')");
                    }
                    JOptionPane.showMessageDialog(null,"TRANSAKSI BERHASIL !!!","INFO",JOptionPane.INFORMATION_MESSAGE);
                    kosong();
                    autoKd();
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null,"TRANSAKSI GAGAL !!!","ERROR",JOptionPane.ERROR_MESSAGE);
                    System.out.println(e);
                }
            }
        }
    }//GEN-LAST:event_btnBytrsActionPerformed

    private void txtPyrKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPyrKeyTyped
        char typed = evt.getKeyChar();
        int cek = txtPyr.getText().length();
        if(cek>=0 && cek<=20){
            if(!Character.isDigit(typed)){
                evt.consume();
            }
        }else if (cek>=20){
            if(Character.isDigit(typed) || !Character.isDigit(typed)){
                evt.consume();
            }
        }
        
        if(cek==0){
            txtPyr.setText("Rp. ");
        }else if(cek==3){
            txtPyr.setText("");
        }
    }//GEN-LAST:event_txtPyrKeyTyped

