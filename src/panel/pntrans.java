    private void caltbyr(){
        int baris = vTabletrs.getRowCount();
        long jlmh,totbyr = 0;
        for(int i=0;i<baris;i++){
            jlmh = Long.valueOf(vTabletrs.getValueAt(i, 4).toString());
            totbyr = totbyr+jlmh;
        }
        txtTotby.setText(kursIndonesia.format(totbyr));
    }
    
